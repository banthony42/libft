#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	size_t			i;

	i = 0;
	str = (unsigned char *)s;
	while (i < n)
	{
		if (str[i] != (unsigned char)c)
			i++;
		else
			return (&str[i]);
	}
	return (NULL);
}
